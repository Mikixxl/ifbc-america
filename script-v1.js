  var RTL_LANGS = { he: 1, ar: 1 };
  var STORAGE_KEY = "ifbc_lang";

  var currentLang = "en";

  function applyLang(lang) {
    if (!TRANSLATIONS[lang]) lang = "en";
    currentLang = lang;
    var t = TRANSLATIONS[lang];

    document.documentElement.lang = lang;
    document.documentElement.dir = RTL_LANGS[lang] ? "rtl" : "ltr";

    // Plain text replacements
    var nodes = document.querySelectorAll("[data-i18n]");
    for (var i = 0; i < nodes.length; i++) {
      var key = nodes[i].getAttribute("data-i18n");
      if (t[key] !== undefined) nodes[i].textContent = t[key];
    }

    // HTML-allowing replacements
    var htmlNodes = document.querySelectorAll("[data-i18n-html]");
    for (var j = 0; j < htmlNodes.length; j++) {
      var hkey = htmlNodes[j].getAttribute("data-i18n-html");
      if (t[hkey] !== undefined) htmlNodes[j].innerHTML = t[hkey];
    }

    // Update lang label in switcher
    var lbl = document.getElementById("langLabel");
    if (lbl) lbl.textContent = LANG_LABELS[lang] || lang.toUpperCase();

    try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
  }

  function pickInitialLang() {
    try {
      var stored = localStorage.getItem(STORAGE_KEY);
      if (stored && TRANSLATIONS[stored]) return stored;
    } catch (e) {}
    var nav = (navigator.languages && navigator.languages[0]) || navigator.language || "en";
    var lc = nav.toLowerCase();
    if (lc.indexOf("zh-tw") === 0 || lc.indexOf("zh-hk") === 0 || lc.indexOf("zh-hant") === 0) return "zh-TW";
    if (lc.indexOf("zh") === 0) return "zh";
    var prefix = lc.split("-")[0];
    if (TRANSLATIONS[prefix]) return prefix;
    return "en";
  }

